#include "stdafx.h";

void main(void)
{
    int A = 10;
    int B = 20;
    cout << A + B << endl;
    cout << A - B << endl;
    cout << A * B << endl;
    cout << B / A << endl;
    cout << 15 / A << endl;
    cout << 15.0 / A << endl;
    cout << B % A << endl;
    cout << ++A << endl;
    cout << --A << endl;
}
